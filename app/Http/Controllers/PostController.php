    {
        $post = Post::find($id);

        // retornar uma VIEW com os dados do Post
        if ($post){
            $author = $post->author()->get()->first();
            return view('showpost', ['post' => $post, 'author' => $author]);
        } else {
            return view('notfound');
        }
    }

